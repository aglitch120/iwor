/** 商品名→一般名変換（部分一致対応） */
export function convertToGeneric(brandName: string): string {
  const trimmed = brandName.trim().replace(/[®️]/g, '').replace(/配合錠|配合カプセル|配合顆粒|注射液|注射用|点滴静注|静注|点滴|錠|カプセル|細粒|散|坐剤|テープ|パッチ|吸入用|吸入|注|OD錠|CR錠|LA錠|SR錠|徐放錠|腸溶錠/g, '').trim()
  // 完全一致
  if (BRAND_TO_GENERIC[trimmed]) return BRAND_TO_GENERIC[trimmed]
  // 部分一致（長い商品名から順にマッチ）
  const sorted = Object.keys(BRAND_TO_GENERIC).sort((a, b) => b.length - a.length)
  for (const brand of sorted) {
    if (trimmed.includes(brand)) return BRAND_TO_GENERIC[brand]
  }
  return brandName.trim() // 変換できなければそのまま
}

/** 処方テキストの一般名一括変換 */
export function convertPrescriptionToGeneric(text: string): string {
  let result = text
  const sorted = Object.entries(BRAND_TO_GENERIC).sort((a, b) => b[0].length - a[0].length)
  for (const [brand, generic] of sorted) {
    result = result.replace(new RegExp(brand, 'g'), generic)
  }
  return result
}
